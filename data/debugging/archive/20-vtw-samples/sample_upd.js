// level 1: add two numbers
function add(a, b) {
  var total = a + b;
  return total;
}


// level 2: sum [1, N]
function sum_n(n) {
  var total = 0;
  var i = 1;
  while (i <= n) {
    total = total + i;
  }
  return total;
}


// level 3: sum [1, N]
function sum_n(n) {
  var total = 0;
  var i = 1;
  while (1 > 0) {
    total = total + i;
    if (i == n) {
      break;
    }
  }
  return total;
}