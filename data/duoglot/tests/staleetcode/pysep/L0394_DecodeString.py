from typing import *
def f_gold(s: str) -> str:
    s1, s2 = [], []
    num, res = 0, ''
    for c in s:
        if c.isdigit():
            num = num * 10 + int(c)
        elif c == '[':
            s1.append(num)
            s2.append(res)
            num, res = 0, ''
        elif c == ']':
            res = s2.pop() + res * s1.pop()
        else:
            res += c
    return res