from typing import *
def f_gold(nums: List[int], k: int) -> int:
    def quick_sort(left, right, k):
        if left == right:
            return nums[left]
        i, j = left - 1, right + 1
        x = nums[(left + right) >> 1]
        while i < j:
            while 1:
                i += 1
                if nums[i] >= x:
                    break
            while 1:
                j -= 1
                if nums[j] <= x:
                    break
            if i < j:
                nums[i], nums[j] = nums[j], nums[i]
        if j < k:
            return quick_sort(j + 1, right, k)
        return quick_sort(left, j, k)
    n = len(nums)
    return quick_sort(0, n - 1, n - k)