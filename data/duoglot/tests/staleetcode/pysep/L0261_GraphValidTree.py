from typing import *
def f_gold(n: int, edges: List[List[int]]) -> bool:
    def find(x):
        if p[x] != x:
            p[x] = find(p[x])
        return p[x]
    p = list(range(n))
    for a, b in edges:
        if find(a) == find(b):
            return False
        p[find(a)] = find(b)
        n -= 1
    return n == 1