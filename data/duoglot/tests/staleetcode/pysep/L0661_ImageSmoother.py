from typing import *
def f_gold(img: List[List[int]]) -> List[List[int]]:
    m, n = len(img), len(img[0])
    ans = [[0] * n for _ in range(m)]
    for i in range(m):
        for j in range(n):
            s = cnt = 0
            for x in range(i - 1, i + 2):
                for y in range(j - 1, j + 2):
                    if 0 <= x < m and 0 <= y < n:
                        cnt += 1
                        s += img[x][y]
            ans[i][j] = s // cnt
    return ans