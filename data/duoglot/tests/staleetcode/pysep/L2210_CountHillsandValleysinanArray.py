from typing import *
def f_gold(nums: List[int]) -> int:
    ans = j = 0
    for i in range(1, len(nums) - 1):
        if nums[i] == nums[i + 1]:
            continue
        if nums[i] > nums[j] and nums[i] > nums[i + 1]:
            ans += 1
        if nums[i] < nums[j] and nums[i] < nums[i + 1]:
            ans += 1
        j = i
    return ans