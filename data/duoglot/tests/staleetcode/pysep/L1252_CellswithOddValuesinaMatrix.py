from typing import *
def f_gold(m: int, n: int, indices: List[List[int]]) -> int:
    g = [[0] * n for _ in range(m)]
    for r, c in indices:
        for i in range(m):
            g[i][c] += 1
        for j in range(n):
            g[r][j] += 1
    return sum(g[i][j] % 2 for i in range(m) for j in range(n))