from typing import *
def f_gold(intervals: List[List[int]]) -> List[List[int]]:
    intervals.sort()
    ans = []
    st, ed = intervals[0]
    for s, e in intervals[1:]:
        if ed < s:
            ans.append([st, ed])
            st, ed = s, e
        else:
            ed = max(ed, e)
    ans.append([st, ed])
    return ans