from typing import *
def f_gold(nums: List[int]) -> int:
    f = sum(i * v for i, v in enumerate(nums))
    n, s = len(nums), sum(nums)
    ans = f
    for i in range(1, n):
        f = f + s - n * nums[n - i]
        ans = max(ans, f)
    return ans
